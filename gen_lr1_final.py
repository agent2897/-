class PDF(FPDF):
    def __init__(self):
        super().__init__()
        self.add_font("S", "",   F + r"\DejaVuSerif.ttf")
        self.add_font("S", "B",  F + r"\DejaVuSerif-Bold.ttf")
        self.add_font("S", "I",  F + r"\DejaVuSerif-Italic.ttf")
        self.add_font("S", "BI", F + r"\DejaVuSerif-BoldItalic.ttf")
        self.set_auto_page_break(True, margin=BM)

    # ── helpers ─────────────────────────────────────────────────────────────

    def t(self, text, sz=12, bold=False, italic=False, align="L", lh=7):
        st = ("B" if bold else "") + ("I" if italic else "")
        self.set_font("S", st, sz)
        self.set_text_color(0, 0, 0)
        self.multi_cell(0, lh, text, align=align, new_x="LMARGIN", new_y="NEXT")

    def par(self, text, indent=0, lh=7):
        """Indented paragraph"""
        self.set_font("S", "", 12)
        self.set_text_color(0, 0, 0)
        if indent:
            self.set_x(self.l_margin + indent)
        self.multi_cell(0, lh, text, new_x="LMARGIN", new_y="NEXT")

    def section(self, text):
        """Numbered section heading"""
        self.ln(4)
        self.set_font("S", "B", 14)
        self.set_text_color(0, 0, 0)
        self.multi_cell(0, 8, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def bullet(self, bold_part, rest, indent=8, lh=7):
        """• BoldPart rest  (inline write for proper wrap)"""
        self.set_x(self.l_margin + indent)
        self.set_font("S", "", 12)
        self.write(lh, "• ")
        self.set_font("S", "B", 12)
        self.write(lh, bold_part)
        self.set_font("S", "", 12)
        self.write(lh, rest)
        self.ln(lh + 2)

    def cat(self, bold_text, indent=5, lh=7):
        """❖ BoldText  — category header"""
        self.ln(2)
        self.set_x(self.l_margin + indent)
        self.set_font("S", "", 12)
        self.write(lh, "❖ ")          # ❖
        self.set_font("S", "B", 12)
        self.write(lh, bold_text)
        self.ln(lh + 1)

    def sub(self, bold_part, rest, indent=15, lh=7):
        """  ➢ BoldPart rest"""
        self.set_x(self.l_margin + indent)
        self.set_font("S", "", 12)
        self.write(lh, "➢ ")          # ➢
        if bold_part:
            self.set_font("S", "B", 12)
            self.write(lh, bold_part)
        self.set_font("S", "", 12)
        self.write(lh, rest)
        self.ln(lh + 1)


